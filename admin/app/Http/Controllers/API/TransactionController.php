    /**
     * Store a newly created resource in storage.
     *
     * @param  \Illuminate\Http\Request  $request
     * @return \Illuminate\Http\Response
     */
    public function store(Request $request)
    {
        try {
            DB::beginTransaction();
            $transaction = new Transaction();
            $transaction->uuid = \Str::uuid();
            $transaction->name = $request->name;
            $transaction->email = $request->email;
            $transaction->number = $request->number;
            $transaction->address = $request->address;
            $transaction->transaction_total = $request->transaction_total;
            $transaction->transaction_status = "pending";
            $transaction->save();

            $transactionDetail = new TransactionDetail();
            $transactionDetail->transactions_id = $transaction->id;
            $transactionDetail->products_id = $request->product_id;
            $transactionDetail->save();

            $transaction->details;

            DB::commit();
            return Json::response($transaction);
        } catch (\Illuminate\Database\Eloquent\ModelNotFoundException $e) {
            DB::rollBack();
            return Json::exception('Error Model ' . $debug = env('APP_DEBUG', false) == true ? $e : '');
        } catch (\Illuminate\Database\QueryException $e) {
            DB::rollBack();
            return Json::exception('Error Query' . $debug = env('APP_DEBUG', false) == true ? $e : '');
        } catch (\ErrorException $e) {
            DB::rollBack();
            return Json::exception('Error Exception ' . $debug = env('APP_DEBUG', false) == true ? $e : '');
        }
    }

